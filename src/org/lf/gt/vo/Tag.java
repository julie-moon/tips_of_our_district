package org.lf.gt.vo;

public class Tag {
	private int tagNo;
	private String tag;
	
	public Tag() {
		
	}

	public int getTagNo() {
		return tagNo;
	}

	public void setTagNo(int tagNo) {
		this.tagNo = tagNo;
	}

	public String getTag() {
		return tag;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}
	
}
